#include<stdio.h>

/**
 * main - print if the number is positive,zero or negative
 *
 * description:using the main function
 * this program prints positive,negative or zero
 * Return: 0
 */
int main(void)
{
	char letter;

	for (letter = 'z' ; letter >= 'a' ; letter--)
	{
		putchar(letter);
	}
	putchar('\n');
	return (0);
}
